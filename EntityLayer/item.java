package EntityLayer;

public class item {
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIname() {
		return iname;
	}
	public void setIname(String iname) {
		this.iname = iname;
	}
	public String getDes() {
		return des;
	}
	public void setDes(String des) {
		this.des = des;
	}
	public int getSellp() {
		return sellp;
	}
	public void setSellp(int sellp) {
		this.sellp = sellp;
	}
	public int getBuyp() {
		return buyp;
	}
	public void setBuyp(int buyp) {
		this.buyp = buyp;
	}
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	private String id;
	private String iname;
	private String des;
	private int sellp;
	private int buyp;
	private int qty;
	
	

}
